import turtle as t

# "python" 출력 거북이
t_python = t.Turtle()
t_python.ht()

# 전구 켜기
def bulb_on() :
        t.shape('turtle')
        t.ht()
        t.pensize(3)
        t.speed(500)
        t.penup()
        t.setposition(-300,-150)
        t.pendown()
        t.begin_fill()
        t.fillcolor('yellow')
        t.setheading(180)
        t.lt(45)
        for cnt in range(4) :
                t.fd(20)
                t.lt(90)
                t.fd(20)
                t.rt(90)
        t.setheading(0)
        t.fd(100)
        t.lt(45)
        for cnt in range(4) :
                t.fd(20)
                t.lt(90)
                t.fd(20)
                t.rt(90)
        t.setheading(90)
        t.fd(50)
        t.setheading(225)
        for cnt in range(7) :
                t.fd(10)
                t.rt(90)
                t.fd(10)
                t.lt(90)
        t.setheading(270)
        t.fd(50)
        t.penup()
        t.fd(20)
        t.setheading(0)
        t.fd(50)
        t.pendown()
        t.circle(80)
        t.end_fill()
        t.bgcolor('white')
        # "python" 문자 넣기
        t_python.penup()
        t_python.setposition(0,20)
        t_python.pendown()
        t_python.pensize(5)
        t_python.pencolor('green')
        t_python.write("Python",False,"center",("",150))

