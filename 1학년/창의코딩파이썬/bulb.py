# 스위치 모양
t_switch = t.Turtle()
t_switch.ht()
t_switch.speed(0)
t_switch.pensize(2)
t_switch.pencolor('red')

# 직사각형
t_switch.penup()
t_switch.setposition(300,-300)
t_switch.pendown()
t_switch.fd(50)
t_switch.lt(90)
t_switch.fd(100)
t_switch.lt(90)
t_switch.fd(50)
t_switch.lt(90)
t_switch.fd(100)
t_switch.bk(50)
t_switch.lt(90)
t_switch.fd(50)
t_switch.bk(25)
t_switch.lt(90)

# ↑ 모양
t_switch.penup()
t_switch.fd(10)
t_switch.pendown()
t_switch.fd(30)
t_switch.setheading(225)
t_switch.fd(10)
t_switch.bk(10)
t_switch.setheading(315)
t_switch.fd(10)
t_switch.bk(10)
t_switch.setheading(270)
t_switch.fd(30)
t_switch.setheading(0)
t_switch.write('on')
t_switch.setheading(270)

# ↓ 모양
t_switch.penup()
t_switch.fd(20)
t_switch.pendown()
t_switch.fd(10)
t_switch.setheading(0)
t_switch.write('off')
t_switch.setheading(270)
t_switch.fd(20)
t_switch.setheading(45)
t_switch.fd(10)
t_switch.bk(10)
t_switch.setheading(135)
t_switch.fd(10)
t_switch.bk(10)

# 키보드 입력
t.onkeypress(bulb_on, 'Up')
t.onkeypress(bulb_off, 'Down')
t.listen()
